let a =3, b =2;

//operador suma
let z = a+b;
console.log("resultado: " +z);

//operador resta
z = a-b;
console.log("resultado: " +z);

//operador multiplicacion
z = a*b;
console.log("resultado: " +z);

//operador division
z = a/b;
console.log("resultado: " +z);

//operador modulo
z = a%b;
console.log("resultado: " +z);

//operador potencia a^b = a**b
z = a**b;
console.log("resultado: " +z);

//operador incremento

//pre-incremento ++variable
z = ++a;
console.log("resultado: " +z);
//post-incremento variable++
//a = 3   b = 2
//primero la variable b = 2 = z
//despues de volver a usar la variable b ya se incrementa
z = b++;
console.log(z);
console.log(b);
//operador resta
z = --a;
console.log("resultado: " +z);